package my;

public class Hello_AO
{

	public static void main(String[] args)
	{
		int a = 10;
		int b = 5;
		int c = a + b;
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = a + b =" + c);
		
		int d = 2;
		int e = 4;
		int f = d * e;
		System.out.println("d = " + d);
		System.out.println("e = " + e);
		System.out.println("f = d * e = " + f);
	}

}